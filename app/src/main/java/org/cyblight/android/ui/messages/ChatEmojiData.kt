    fun allEmojis(): List<String> = categories.flatMap { it.emojis }.distinct()

    fun filterEmojis(query: String, categoryIndex: Int): List<String> {
        val normalized = query.trim().lowercase()
        if (normalized.isEmpty()) {
            return categories.getOrNull(categoryIndex)?.emojis.orEmpty()
        }
        return categories.flatMap { category ->
            category.emojis.filter { emoji ->
                emoji.contains(normalized) ||
                    category.searchTags.any { tag -> tag.lowercase().contains(normalized) }
            }
        }.distinct()
    }
